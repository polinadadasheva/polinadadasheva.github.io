/* =============================================================
   ЭТОТ ФАЙЛ ОТВЕЧАЕТ ЗА "ПОВЕДЕНИЕ" СТРАНИЦЫ
   (а не за внешний вид — вид настраивается в style.css)
============================================================= */

// Находим в HTML-коде блок, внутри которого "едут" экраны
const scrollArea = document.getElementById('scrollArea');

/* -------------------------------------------------------------
   1. ПРЕВРАЩАЕМ КОЛЕСИКО МЫШИ В ГОРИЗОНТАЛЬНУЮ ПРОКРУТКУ

   По умолчанию, когда человек крутит колесико мыши вниз, браузер
   прокручивает страницу вниз. Мы "перехватываем" этот момент и
   вместо этого двигаем страницу в сторону (влево/вправо).
------------------------------------------------------------- */
scrollArea.addEventListener('wheel', (event) => {
  // event.deltaY — число, которое говорит, на сколько "пунктов"
  // покрутили колесико по вертикали (положительное — вниз, отрицательное — вверх)
  if (event.deltaY !== 0) {
    event.preventDefault(); // отключаем обычную вертикальную прокрутку страницы
    scrollArea.scrollLeft += event.deltaY; // двигаем экраны на ту же величину, но по горизонтали
  }
}, { passive: false });
// "passive: false" — техническая настройка, без неё браузер
// не даст нам отключить обычное вертикальное прокручивание.

/* -------------------------------------------------------------
   2. ТО ЖЕ САМОЕ, НО ДЛЯ СВАЙПОВ ПАЛЬЦЕМ (телефон/планшет)
------------------------------------------------------------- */
let touchStartY = 0; // здесь запоминаем, где палец коснулся экрана

scrollArea.addEventListener('touchstart', (event) => {
  touchStartY = event.touches[0].clientY;
}, { passive: true });

scrollArea.addEventListener('touchmove', (event) => {
  const touchCurrentY = event.touches[0].clientY;

  // На сколько пикселей сдвинулся палец с прошлого момента
  const deltaY = touchStartY - touchCurrentY;

  scrollArea.scrollLeft += deltaY; // двигаем экраны по горизонтали на эту же величину
  touchStartY = touchCurrentY;     // запоминаем новую точку для следующего шага свайпа
}, { passive: true });

